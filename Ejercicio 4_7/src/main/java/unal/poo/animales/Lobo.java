package unal.poo.animales;

public class Lobo extends Canido {

    @Override
    public String getNombreCientifico() {
         return "Canis lupus";
    }

    @Override
    public String getSonido() {
         return "Aullido";
    }

    @Override
    public String getAlimentos() {
         return "Carnivoro";
    }

    @Override
    public String getHabitad() {
         return "Bosque";
    }
    
}
